#include "monty.h"
/**
 * monty_queue - prints the top
 * @stack: stack stack
 * @line_number: line_number
 * Return: no return
*/
void monty_queue(stack_t **stack, unsigned int line_number)
{
	(void)stack;
	(void)line_number;
	glinput.swtch = 1;
}

/**
 * monty_stack - prints the top
 * @stack: stack pointer
 * @line_number: line_number
 * Return: no return
*/
void monty_stack(stack_t **stack, unsigned int line_number)
{
	(void)stack;
	(void)line_number;
	glinput.swtch = 0;
}

