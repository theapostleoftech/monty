#include "monty.h"
/**
 * monty_stack - prints the top of the stack
 * @stack: pointer to the stack
 * @line_number: line number
 * Return: nothing
 */
void monty_stack(stack_t **stack, unsigned int line_number)
{
	(void)stack;
	(void)line_number;
	inputs.switch_flag = 0;
}


/**
 * monty_queue - prints the node to the tail of the stack
 * @stack: tpointer to the the stack
 * @line_number: line number
 * Return: nothing
 */
void monty_queue(stack_t **stack, unsigned int line_number)
{
	(void)stack;
	(void)line_number;
	inputs.switch_flag = 1;
}

